# Los tres operadores logicos que existen en Python son los siguientes:
# and = Operador logico que necesita que las dos comparaciones se cumplan para que la condicion se cumpla

# or = Operador logico que necesita que una de las dos comparaciones se cumplan para que la condicion se cumpla

# not = Nega la comparacion que se esta condicionando

